<strong><h3>FAQ Affiliate :</h3> </strong><br /><br />

<p id="aff1">
<strong>- Apa itu Affiliate System</strong><br />
Affiliate system adalah sistem yang disediakan oleh kueibuhasan.com bagi mereka yang ingin mendapatkan penghasilan tambahan dari kueibuhasan.com dengan membantu menjualkan produk-produk pilihan kueibuhasan.com<br /><br />
</p>
<br />

<p id="aff2">
<strong>- Berapa Komisi yang akan di dapatkan member Affiliate apabila telah berhasil menjual produk kueibuhasan.com </strong><br />
Komisi yang akan di dapatkan oleh para Affiliater kueibuhasan beragam mulai dari 3 % hingga 40 % dari harga produk yang berhasil di jual. Ada produk yang bisa memberikan komisi hanya Rp. 1000 per- penjualan, tetapi ada pula produk yang bisa memberikan komisi hingga  mencapai lebih dari Rp.  1.000.000 per-penjualan.<br /><br />
</p>
<br />

<p id="aff3">
<strong>- Kapan Komisi Affiliate akan diberikan </strong><br />
Komisi Affiliate akan diberikan kepada Affiliater kueibuhasan setiap bulannya pada tanggal 5 di awal bulan dengan syarat bahwa komisi affiliate member tersebut sudah mencapai besar minimal komisi yang telah di tentukan oleh affiliate member itu sendiri di Affiliate areanya dan settingan status Pembayaran tidak dalam keadaan "Tunda".<br /><br />
</p>
<br />

<p id="aff4">
<strong>- Berapa besar minimal komisi pembayaran yang dapat dipilih oleh member Affiliate </strong><br />
Besar minimal komisi pembayaran yang dapat dipilih oleh member Affiliate adalah <br />
a.	Rp. 200.000<br />
b.	Rp. 500.000<br />
c.	Rp.1.000.000<br />
d.	Rp. 2.000.000<br />
e.	Rp. 5.000.000<br /><br />
</p>
<br />

<p id="aff5">
<strong>- Apa saja metode pembayaran komisi Affiliate yang disediakan di kueibuhasan.com</strong><br />
Metode pembayaran komisi affiliate yang disediakan di kueibuhasan.com diantaranya adalah transfer antar bank dan transfer melalui account paypal.<br />
</p>

